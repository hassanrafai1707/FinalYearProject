// --- Helper: Display question table ---
function displayQuestionTable(containerId, questions) {
    const container = document.getElementById(containerId);
    if (!questions) {
        container.innerHTML = '<div class="alert alert-warning">No results.</div>';
        return;
    }

    // Handle both direct array and wrapped response
    let list = [];
    if (Array.isArray(questions)) {
        list = questions;
    } else if (questions.data && Array.isArray(questions.data)) {
        list = questions.data;
    } else if (questions.content && Array.isArray(questions.content)) {
        list = questions.content;
    }

    if (list.length === 0) {
        container.innerHTML = '<div class="alert alert-warning">No questions found.</div>';
        return;
    }

    let html = `<table class="data-table">
    <thead>
         <tr>
            <th>ID</th>
            <th>Question</th>
            <th>Subject Name</th>
            <th>Subject Code</th>
            <th>CO</th>
            <th>Marks</th>
            <th>Cognitive Level</th>
         </tr>
    </thead>
    <tbody>`;

    list.forEach(q => {
        html += `<tr>
            <td>${q.id || '-'}</td>
            <td>${q.questionBody || q.body || '-'}</td>
            <td>${q.subjectName || '-'}</td>
            <td>${q.subjectCode || '-'}</td>
            <td>${q.mappedCO || '-'}</td>
            <td>${q.questionMarks || '-'}</td>
            <td>${q.cognitiveLevel || '-'}</td>
        </tr>`;
    });

    html += '</tbody></table>';
    container.innerHTML = html;
}

// --- Helper: Display paper table ---
function displayPaperTable(containerId, papers) {
    const container = document.getElementById(containerId);
    if (!papers) {
        container.innerHTML = '<div class="alert alert-warning">No results.</div>';
        return;
    }

    // Handle both direct array and wrapped response
    let list = [];
    if (Array.isArray(papers)) {
        list = papers;
    } else if (papers.data && Array.isArray(papers.data)) {
        list = papers.data;
    } else if (papers.content && Array.isArray(papers.content)) {
        list = papers.content;
    }

    if (list.length === 0) {
        container.innerHTML = '<div class="alert alert-warning">No papers found.</div>';
        return;
    }

    let html = `<table class="data-table">
    <thead>
         <tr>
            <th>ID</th>
            <th>Exam Title</th>
            <th>Subject</th>
            <th>Total Marks</th>
            <th>Generated By</th>
            <th>Approved</th>
            <th>Approved By</th>
         </tr>
    </thead>
    <tbody>`;

    list.forEach(p => {
        let totalMarks = p.totalMarks || '-';
        if (!p.totalMarks && p.listOfQuestion && Array.isArray(p.listOfQuestion)) {
            totalMarks = p.listOfQuestion.reduce((sum, q) => sum + (q.questionMarks || 0), 0);
        }

        const approved = p.approved ? '<span class="status-badge active">Yes</span>' : '<span class="status-badge suspended">No</span>';
        const generatedBy = p.generatedBy ? (p.generatedBy.email || p.generatedBy.id || '-') : '-';
        const approvedBy = p.approvedBy ? (p.approvedBy.email || p.approvedBy.id || '-') : '-';

        let subjectInfo = p.subjectName || p.subjectCode || '-';
        if (!subjectInfo && p.listOfQuestion && p.listOfQuestion.length > 0) {
            subjectInfo = p.listOfQuestion[0].subjectName || p.listOfQuestion[0].subjectCode || '-';
        }

        html += `<tr>
            <td>${p.id || '-'}</td>
            <td>${p.examTitle || '-'}</td>
            <td>${subjectInfo}</td>
            <td>${totalMarks}</td>
            <td>${generatedBy}</td>
            <td>${approved}</td>
            <td>${approvedBy}</td>
        </tr>`;
    });

    html += '</tbody></table>';
    container.innerHTML = html;
}

// --- Helper: Display single paper card ---
function displayPaperCard(containerId, p) {
    const container = document.getElementById(containerId);
    if (!p) {
        container.innerHTML = '<div class="alert alert-warning">Paper not found.</div>';
        return;
    }

    const paper = p.data || p;

    const approved = paper.approved ? 'Yes' : 'No';
    const generatedBy = paper.generatedBy ? (paper.generatedBy.email || paper.generatedBy.name || paper.generatedBy.id || '-') : '-';
    const approvedBy = paper.approvedBy ? (paper.approvedBy.email || paper.approvedBy.name || paper.approvedBy.id || '-') : '-';

    let totalMarks = paper.totalMarks || '-';
    if (!paper.totalMarks && paper.listOfQuestion && Array.isArray(paper.listOfQuestion)) {
        totalMarks = paper.listOfQuestion.reduce((sum, q) => sum + (q.questionMarks || 0), 0);
    }

    let subjectInfo = paper.subjectName || paper.subjectCode || '-';
    if (!subjectInfo && paper.listOfQuestion && paper.listOfQuestion.length > 0) {
        subjectInfo = paper.listOfQuestion[0].subjectName || paper.listOfQuestion[0].subjectCode || '-';
    }

    container.innerHTML = `
    <div class="card" style="margin-top:10px;">
        <h2><i class="fas fa-file-alt"></i> ${paper.examTitle || 'Paper #' + paper.id}</h2>
        <p><strong>ID:</strong> ${paper.id || '-'}</p>
        <p><strong>Subject:</strong> ${subjectInfo}</p>
        <p><strong>Total Marks:</strong> ${totalMarks}</p>
        <p><strong>Generated By:</strong> ${generatedBy}</p>
        <p><strong>Approved:</strong> ${approved}</p>
        <p><strong>Approved By:</strong> ${approvedBy}</p>
        ${paper.comment ? `<p><strong>Comment:</strong> ${paper.comment}</p>` : ''}
        ${paper.listOfQuestion ? `
            <div style="margin-top: 15px;">
                <h3>Questions (${paper.listOfQuestion.length})</h3>
                <div style="max-height: 400px; overflow-y: auto;">
                    ${displayQuestionList(paper.listOfQuestion)}
                </div>
            </div>
        ` : ''}
    </div>`;
}

// Helper function to display questions in paper card
function displayQuestionList(questions) {
    if (!questions || questions.length === 0) return '<p>No questions found.</p>';

    let html = '<table class="mini-table"><thead><tr>';
    html += '<th>ID</th><th>Question</th><th>Marks</th><th>CO</th><th>Level</th>';
    html += '</tr></thead><tbody>';

    questions.forEach(q => {
        html += `<tr>
            <td>${q.id || '-'}</td>
            <td>${q.questionBody || '-'}</td>
            <td>${q.questionMarks || '-'}</td>
            <td>${q.mappedCO || '-'}</td>
            <td>${q.cognitiveLevel || '-'}</td>
        </tr>`;
    });

    html += '</tbody></table>';
    return html;
}

// --- Helper: Show result ---
function showResult(containerId, message, isError = false) {
    const el = document.getElementById(containerId);
    if (el) {
        const alertClass = isError ? 'alert-error' : 'alert-success';
        el.innerHTML = `<div class="alert ${alertClass}">
            <i class="fas ${isError ? 'fa-exclamation-triangle' : 'fa-check-circle'}"></i>
            ${message}
        </div>`;

        // Auto-clear success messages after 3 seconds
        if (!isError) {
            setTimeout(() => {
                if (el.innerHTML.includes(message)) {
                    el.innerHTML = '';
                }
            }, 3000);
        }
    }
}

