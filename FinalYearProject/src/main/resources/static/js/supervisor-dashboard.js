// All Questions Paged
async function loadAllQuestionsPaged() {
  try {
    const pageNo = parseInt(document.getElementById('pageNoAllQuestions').value) - 1;
    const size = parseInt(document.getElementById('pageSizeAllQuestions').value);
    const data = await SupervisorAPI.getAllQuestionsPaged(pageNo, size);
    displayQuestionTable('allQuestionsPagedResult', data);
  } catch (e) {
    showResult('allQuestionsPagedResult', e.message, true);
  }
}

// Question By ID
async function findQuestionById() {
  try {
    const id = document.getElementById('questionId').value.trim();
    if (!id) { showResult('questionByIdResult', 'Please enter a question ID.', true); return; }
    const data = await SupervisorAPI.getQuestionById(id);
    const container = document.getElementById('questionByIdResult');
    if (!data) { container.innerHTML = '<div class="alert alert-warning">Question not found.</div>'; return; }
    container.innerHTML = `
      <div class="card" style="margin-top:10px;">
        <h2><i class="fas fa-question-circle"></i> Question #${data.id || '?'}</h2>
        <p><strong>Question:</strong> ${data.questionBody || data.body || '-'}</p>
        <p><strong>Subject:</strong> ${data.subjectName || '-'} (${data.subjectCode || '-'})</p>
        <p><strong>Mapped CO:</strong> ${data.mappedCO || '-'}</p>
        <p><strong>Marks:</strong> ${data.marks || '-'}</p>
        <p><strong>Cognitive Level:</strong> ${data.cognitiveLevel || '-'}</p>
      </div>`;
  } catch (e) {
    showResult('questionByIdResult', e.message, true);
  }
}

