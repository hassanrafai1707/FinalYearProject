// --- Helper: Display question table ---
function displayQuestionTable(containerId, questions) {
  const container = document.getElementById(containerId);
  if (!questions) { container.innerHTML = '<div class="alert alert-warning">No results.</div>'; return; }
  const list = Array.isArray(questions) ? questions : (questions.content || []);
  if (list.length === 0) { container.innerHTML = '<div class="alert alert-warning">No questions found.</div>'; return; }
  let html = `<table><thead><tr>
    <th>ID</th><th>Question</th><th>Subject Name</th><th>Subject Code</th><th>CO</th><th>Marks</th><th>Cognitive Level</th>
  </tr></thead><tbody>`;
  list.forEach(q => {
    html += `<tr>
      <td>${q.id || '-'}</td>
      <td>${q.questionBody || q.body || '-'}</td>
      <td>${q.subjectName || '-'}</td>
      <td>${q.subjectCode || '-'}</td>
      <td>${q.mappedCO || '-'}</td>
      <td>${q.marks || '-'}</td>
      <td>${q.cognitiveLevel || '-'}</td>
    </tr>`;
  });
  html += '</tbody></table>';
  container.innerHTML = html;
}

// --- Helper: Display paper table ---
function displayPaperTable(containerId, papers) {
  const container = document.getElementById(containerId);
  if (!papers) { container.innerHTML = '<div class="alert alert-warning">No results.</div>'; return; }
  const list = Array.isArray(papers) ? papers : (papers.content || []);
  if (list.length === 0) { container.innerHTML = '<div class="alert alert-warning">No papers found.</div>'; return; }
  let html = `<table><thead><tr>
    <th>ID</th><th>Exam Title</th><th>Subject</th><th>Total Marks</th><th>Generated By</th><th>Approved</th><th>Approved By</th>
  </tr></thead><tbody>`;
  list.forEach(p => {
    const approved = p.approved ? '<span class="status-badge active">Yes</span>' : '<span class="status-badge suspended">No</span>';
    html += `<tr>
      <td>${p.id || '-'}</td>
      <td>${p.examTitle || '-'}</td>
      <td>${p.subjectName || p.subjectCode || '-'}</td>
      <td>${p.totalMarks || '-'}</td>
      <td>${p.generatedByEmail || p.generatedById || '-'}</td>
      <td>${approved}</td>
      <td>${p.approvedByEmail || p.approvedById || '-'}</td>
    </tr>`;
  });
  html += '</tbody></table>';
  container.innerHTML = html;
}

// --- Helper: Display single paper card ---
function displayPaperCard(containerId, p) {
  const container = document.getElementById(containerId);
  if (!p) { container.innerHTML = '<div class="alert alert-warning">Paper not found.</div>'; return; }
  const approved = p.approved ? 'Yes' : 'No';
  container.innerHTML = `
    <div class="card" style="margin-top:10px;">
      <h2><i class="fas fa-file-alt"></i> ${p.examTitle || 'Paper #' + p.id}</h2>
      <p><strong>ID:</strong> ${p.id || '-'}</p>
      <p><strong>Subject:</strong> ${p.subjectName || p.subjectCode || '-'}</p>
      <p><strong>Total Marks:</strong> ${p.totalMarks || '-'}</p>
      <p><strong>Generated By:</strong> ${p.generatedByEmail || p.generatedById || '-'}</p>
      <p><strong>Approved:</strong> ${approved}</p>
      <p><strong>Approved By:</strong> ${p.approvedByEmail || p.approvedById || '-'}</p>
    </div>`;
}

// --- Helper: Show result ---
function showResult(containerId, message, isError = false) {
  const el = document.getElementById(containerId);
  if (el) el.innerHTML = `<div class="alert ${isError ? 'alert-error' : 'alert-success'}">${message}</div>`;
}

