function showSection(sectionId) {
    // Hide all sections
    document.querySelectorAll('.section').forEach(section => {
        section.classList.remove('active');
    });

    // Show selected section
    const selectedSection = document.getElementById(sectionId);
    if (selectedSection) {
        selectedSection.classList.add('active');
    }

    // Update active menu item
    document.querySelectorAll('.sidebar ul li a').forEach(link => {
        link.classList.remove('active');
    });

    // Update page title
    const titles = {
        allQuestionsPaged: 'All Questions (Paged)',
        questionById: 'Question By ID',
        bySubjectCodePaged: 'By Subject Code (Paged)',
        bySubjectCodeCOPaged: 'Code & CO (Paged)',
        bySubjectNamePaged: 'By Subject Name (Paged)',
        byCreatedByEmail: 'By Creator Email',
        byCreatedById: 'By Creator ID',
        allPapersPaged: 'All Papers (Paged)',
        paperById: 'Paper By ID',
        paperByTitle: 'Paper By Title',
        byGeneratorEmailPaged: 'By Generator Email (Paged)',
        byGeneratorIdPaged: 'By Generator ID (Paged)',
        byApproverEmailPaged: 'By Approver Email (Paged)',
        byApproverIdPaged: 'By Approver ID (Paged)',
        approvedPapersPaged: 'Approved Papers (Paged)',
        pendingPapersPaged: 'Pending Papers (Paged)',
        approveById: 'Approve/Reject by ID',
        approveByTitle: 'Approve/Reject by Title',
        updateEmail: 'Update Email',
        updatePassword: 'Update Password'
    };

    const pageTitle = document.getElementById('pageTitle');
    if (pageTitle) {
        pageTitle.textContent = titles[sectionId] || 'Supervisor Dashboard';
    }
}

function toggleSidebar() {
    const sidebar = document.getElementById('sidebar');
    const mainContent = document.getElementById('mainContent');
    if (sidebar && mainContent) {
        sidebar.classList.toggle('collapsed');
        mainContent.classList.toggle('expanded');

        const toggleBtn = document.querySelector('#toggleBtn i');
        if (toggleBtn) {
            if (sidebar.classList.contains('collapsed')) {
                toggleBtn.classList.remove('fa-chevron-left');
                toggleBtn.classList.add('fa-chevron-right');
            } else {
                toggleBtn.classList.remove('fa-chevron-right');
                toggleBtn.classList.add('fa-chevron-left');
            }
        }
    }
}

function toggleMobileMenu() {
    const sidebar = document.getElementById('sidebar');
    if (sidebar) {
        sidebar.classList.toggle('show');
    }
}

